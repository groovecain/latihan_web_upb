            Menabung adalah menyimpan uang di celengan, bank, dan sebagainya. Namun untuk generasi milenial dan Z yang kerap konsumtif, menabung merupakan hal yang sulit untuk dilakukan. Padahal dari kecil mereka sudah diajarkan untuk menabung. Namun masih saja generasi milenial dan gen Z kesulitan menabung. Milenial adalah mereka yang lahir di rentang waktu 1980-1995. Generasi ini tumbuh di era digital dan dikenal nyaman dengan teknologi dan media sosial. Sedangkan gen Z mereka yang lahir dari rentang waktu 1996-2015. Gen Z tumbuh cenderung tertarik dengan teknologi sejak masih kanak-kanak. Berikut manfaat menabung untuk generasi Milenial dan Gen Z dilansir beberapa sumber:
            1. Memiliki Simpanan untuk Keadaan Darurat

            Berbagai keadaan darurat atau mendesak sangat mungkin terjadi dalam kehidupan, misalnya kendaraan rusak karena kecelakaan, biaya perawatan rumah sakit, dan masih banyak lainnya. Dengan memiliki tabungan seseorang dapat menggunakan simpanan untuk pengeluaran tidak terduga tanpa harus memotong biaya rutin bulanan.
            2. Meminimalisir Risiko saat Kehilangan Pekerjaan

            Kehilangan pekerjaan sangat mungkin dialami oleh semua orang. Kebiasaan menabung ini dapat mengurangi perasaan was-was serta risiko yang muncul saat seseorang kehilangan pekerjaan. Setidaknya, uang di tabungan dapat membantu biaya pengeluaran sambil mencari kerja baru atau untuk modal usaha.
            3. Jaminan Hari Pensiun

            Manfaat menabung selanjutnya adalah sebagai jaminan ketika sudah pensiun. Tentu hal ini berdampak panjang karena seseorang tidak mungkin bekerja selamanya. Dengan menabung sedikit demi sedikit, akan terkumpul simpanan yang cukup untuk hari tua nanti.

            4. Investasi Jangka Panjang
            Menabung adalah kegiatan jangka panjang karena hasilnya akan terasa dalam jangka panjang. Sebagai contoh seseorang bisa menempatkan tabungan khusus investasi, seperti deposito, reksadana, ataupun bisnis sektor riil yang prospektif lainnya.
            5. Dana untuk Keperluan yang Diinginkan

            Tabungan tidak hanya untuk keperluan yang dibutuhkan, tetapi bisa juga untuk barang yang diinginkan. Misalnya untuk membeli kendaraan, dana berlibur, biaya kuliah, dan banyak lainnya.
            6. Memberikan Rasa Tenang

            Manfaat menabung yang tidak kalah pentingnya yakni untuk kebebasan finansial seseorang.Tentu akan ada perasaan tenang, aman, dan nyaman saat memiliki simpanan yang cukup. Hidup juga akan lebih mandiri dan tidak bergantung dengan siapa pun.
